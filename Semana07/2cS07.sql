FROM T,V;

-- pi_{t1,v1}(T x V)
SELECT T1,V1
FROM (SELECT T1,T2,T3,V1,V2,V3
FROM T,V);

-- pi_{v3,v1}(V)
SELECT V3,V1
FROM V;

-- P <- pi_{t1,v1}(T x V) inters pi_{v3,v1}(V)
SELECT V3,V1
FROM V
WHERE (V3,V1) IN (SELECT T1,V1
FROM (SELECT T1,T2,T3,V1,V2,V3
FROM T,V));

